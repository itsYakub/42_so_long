#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		len;
	int		i;
	char	*result;

	len = ft_strlen(s);
	result = (char *) malloc((len + 1) * sizeof (char));
	if (!result)
		return (NULL);
	i = 0;
	while (i < len)
	{
		result[i] = s[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
